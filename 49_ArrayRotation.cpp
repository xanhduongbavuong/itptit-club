using namespace std;
typedef unsigned long long ull;

int main(){ //not on SPOJ
	int t;
	cin >> t;
	while(t--){
		ll n;
		cin >> n;
		ll a[n], s = 0, s1 = 0;
		for(ll i = 0; i < n; i++){
			cin >> a[i];
			s += a[i];
			s1 += a[i] * i;
		}
		ll ans = s1;
		for(ll i = 0; i < n - 1; i++){
			s1 = s1 - s + a[i] * n;
			ans = max(ans, s1);
		}
		cout << ans << endl;
	}
	return 0;
}

